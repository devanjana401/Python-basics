# int
a = 10
print(a)        # Output: 10

# float
b = 3.14
print(b)        # Output: 3.14

# complex
c = 2 + 3j
print(c)        # Output: (2+3j)

# str
d = "Hello"
print(d)        # Output: Hello

# bool
e = True
print(e)        # Output: True

# list
f = [1, 2, 3]
print(f)        # Output: [1, 2, 3]

# tuple
g = (1, 2, 3)
print(g)        # Output: (1, 2, 3)

# set
h = {1, 2, 3}
print(h)        # Output: {1, 2, 3}

# dict
i = {"a": 1, "b": 2}
print(i)        # Output: {'a': 1, 'b': 2}

# NoneType
j = None
print(j)        # Output: None
